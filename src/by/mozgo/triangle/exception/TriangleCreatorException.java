package by.mozgo.triangle.exception;

/**
 * @author Andrei Mozgo
 */
public class TriangleCreatorException extends Exception {
    public TriangleCreatorException() {
        super();
    }

    public TriangleCreatorException(String message) {
        super(message);
    }

    public TriangleCreatorException(String message, Throwable cause) {
        super(message, cause);
    }

    public TriangleCreatorException(Throwable cause) {
        super(cause);
    }

}
